import UIKit

extension UIColor {
    class func redButton() ->UIColor {
        let color = UIColor(red: 0.853, green: 0.052, blue: 0.101, alpha: 1.0)
        return color
    }
    
    class func grayButton() ->UIColor {
        let color = UIColor(red: 0.482, green: 0.482, blue: 0.482, alpha: 1.0)
        return color
    }
    class func grayText() ->UIColor {
        let color = UIColor(red: 0.51, green: 0.51, blue: 0.51, alpha: 1.0)
        return color
    }
    
    class func grayView() ->UIColor {
        let color = UIColor(red: 0.917, green: 0.921, blue: 0.93, alpha: 1.0)
        return color
    }
}

